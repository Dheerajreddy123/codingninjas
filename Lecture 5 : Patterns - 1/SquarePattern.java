import java.util.*;

public class Solution {


	public static void main(String[] args) {
        int i,j,N;
        Scanner s=new Scanner(System.in);
        N=s.nextInt();
		for(i=1;i<=N;i++){
          for(j=1;j<=N;j++){
            System.out.print(N);
        } 
            System.out.print("\n");
        }
		
	}

}
